# Health Data Breach

# %% [markdown]
# In 2015, Premera Blue Cross experienced a cyberattack that exposed the data of approximately 11 million people. Hackers gained access to names, addresses, birth dates, Social Security numbers, and bank account information. The breach went undetected for months.
# The consequences included identity theft risks, lawsuits, and significant financial costs related to recovery and monitoring services. Patients lost trust in the insurer’s ability to protect extremely sensitive personal information.
# Prevention could have included better intrusion detection systems, timely patching of known vulnerabilities, and encryption of sensitive data. Regular penetration testing and stronger internal security policies would also have reduced the impact.
# 

